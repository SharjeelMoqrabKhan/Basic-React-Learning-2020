export const Sum = (num1, num2) => {
  let res = num1 + num2;
  return res;
};

export const Subtraction = (num1, num2) => {
  let res = num1 - num2;
  return res;
};

export const Multi = (num1, num2) => {
  let res = num1 + num2;
  return res;
};

export const Divi = (num1, num2) => {
  let res = num1/num2;
  return Math.round(res);
};
